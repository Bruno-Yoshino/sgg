    private void btnLocCidadeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLocCidadeActionPerformed
        ConsultaPadrao consCidade = new ConsultaPadrao(null, true);
        String[] vet = new String[2];
        vet[0] = "Código";
        vet[1] = "Cidade";
        consCidade.configuraOpcoes(vet, 2, 1, "Cidade", false);
        consCidade.verificaconsulta(true);
        consCidade.setVisible(true);
        if (consCidade.getCodigo() != 0)
        {
            ec.localizarCidade(consCidade.getCodigo());
            consCidade.dispose();
            txtCodCid.setText(""+ec.getEmp().getCid().getCodigo());
            txtCidade.setText(ec.getEmp().getCid().getNome());
            txtUf.setText(ec.getEmp().getCid().getE().getSigla());
        }
        else
        {
            consCidade.dispose();
            btnLocCidade.requestFocus();
        }
    }//GEN-LAST:event_btnLocCidadeActionPerformed


    private void btnLocalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLocalizarActionPerformed
        ConsultaPadrao consDS = new ConsultaPadrao(null, true);
        String[] vet = new String[1];
        vet[0] = "Detalhe";
        consDS.configuraOpcoes(vet, 1, 0, "DetalheServico", false);
        consDS.verificaconsulta(true);
        consDS.setVisible(true);
        if (consDS.getCodigo() != 0)
        {
            txtCodigo.setText(String.valueOf(consDS.getCodigo()));
            consDS.dispose();
            txtCodigoFocusLost(null);
            sc.Alter(jPanel2.getComponents());
        }
        else
        {
            consDS.dispose();
            btnLocalizar.requestFocus();
        }
    }//GEN-LAST:event_btnLocalizarActionPerformed


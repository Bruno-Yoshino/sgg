    private void txtCodigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCodigoFocusLost
        if(!txtCodigo.getText().trim().equals("0"))
        {
            dsc.LocalizaDS(Integer.parseInt(txtCodigo.getText()));
            if(dsc.getP()!= null)
            {
                txtNome.setText(dsc.getP().getDescricao());
                cbStatus.setSelected(dsc.getP().getStatus());
                sc.HabilityComponents(jPanel1.getComponents(), false);
                sc.Alter(jPanel2.getComponents());
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Detalhe não Encontrado", "Informação", JOptionPane.INFORMATION_MESSAGE);
                limpar();
                btnNovo.requestFocus();
            }
        }
    }//GEN-LAST:event_txtCodigoFocusLost


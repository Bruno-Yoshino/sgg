    private void levelアクセス(int soma)
    {
        if(soma == 64)
        {
            cbAdm.setSelected(true);
        }
        else
        {
            if(soma >= 16)
            {
                cbMovimentacao.setSelected(true);
                soma -= 16;
            }
            if(soma >= 8)
            {
                cbRelatorio.setSelected(true);
                soma -= 8;
            }
            if(soma >= 4)
            {
                cbConsulta.setSelected(true);
                soma -= 4;
            }
            if(soma >= 2)
            {
                cbCadastro.setSelected(true);
                soma = 0;
            }
        }
    }


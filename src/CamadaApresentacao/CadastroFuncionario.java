    private void txtCodigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCodigoFocusLost
        if (!txtCodigo.getText().equals("0"))
        {
            fc.localizar(Integer.parseInt(txtCodigo.getText()));
            if (fc.getFunc() != null)
            {
                if(fc.getFunc().getDtdemicao()!= null)
                {
                    if(JOptionPane.showConfirmDialog(null, "Este funcinario ele foi demitido. Deseja Contratar Novamente?", "Confirmação", JOptionPane.INFORMATION_MESSAGE) == JOptionPane.YES_OPTION)
                    {
                        fc.getFunc().setCodigo(0);
                        if(fc.gravar())
                        {
                            m.InformationMessage("Funcioanrio Contratado com Sucesso", "Informação");
                        }
                        else
                        {
                            m.ErroMessage("Erro ao Contratar", "Erro");
                        }
                    }
                    else
                    {
                        limpar();
                        btnNovo.requestFocus();
                    }
                }
                else
                {
                    
                    ImageIcon icon = new ImageIcon(fc.getFunc().getCaminho());
                   // lFoto.setIcon(new ImageIcon(icon.getScaledInstance(lFoto.getWidth(),lFoto.getHeight())));
                    lFoto.setIcon(new ImageIcon(icon.getImage().getScaledInstance(lFoto.getWidth(), lFoto.getHeight(), Image.SCALE_DEFAULT))); // Utilizado para recuperar imagem.
                    txtCodigo.setText(""+fc.getFunc().getCodigo()); 
                    txtCidade.setText(fc.getFunc().getCid().getNome()); 
                    txtUf.setText(fc.getFunc().getCid().getE().getSigla()); 
                    txtNome.setText(fc.getFunc().getNome()); 
                    txtUsuario.setText(fc.getFunc().getLogin()); 
                    txtSenha.setText(fc.getFunc().getSenha()); 
                    soma = fc.getFunc().getNivel(); 
                    txtCpf.setText(fc.getFunc().getCpf()); 
                    txtRg.setText(fc.getFunc().getRg()); 
                    txtOrgemi.setText(fc.getFunc().getOrgemi()); 
                    txtEndereco.setText(fc.getFunc().getEndereco()); 
                    txtNumero.setText(""+fc.getFunc().getNumero()); 
                    txtComplemento.setText(fc.getFunc().getComplemento()); 
                    txtTelefone.setText(fc.getFunc().getTelefone());
                    txtCelular.setText(fc.getFunc().getCelular()); 
                    txtCaminho.setText(fc.getFunc().getCaminho()); 
                    txtCep.setText(fc.getFunc().getCep());
                    txtCodCid.setText(""+fc.getFunc().getCid().getCodigo());
                    levelアクセス(soma); // Acesso de nivel

                    sc.HabilityComponents(jPanel1.getComponents(), false);
                    sc.Alter(jPanel2.getComponents());
                }
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Funcionario não Encontrado", "Informação", JOptionPane.INFORMATION_MESSAGE);
                limpar();
                btnNovo.requestFocus();
            }
        }
    }//GEN-LAST:event_txtCodigoFocusLost

